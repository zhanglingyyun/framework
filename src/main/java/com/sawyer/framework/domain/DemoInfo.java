package com.sawyer.framework.domain;

public class DemoInfo
{
	private long id;
	
	private long demo_id;
	
	private String demo_info_name;

	public long getId()
	{
		return id;
	}

	public void setId(long id)
	{
		this.id = id;
	}

	public long getDemo_id()
	{
		return demo_id;
	}

	public void setDemo_id(long demo_id)
	{
		this.demo_id = demo_id;
	}

	public String getDemo_info_name()
	{
		return demo_info_name;
	}

	public void setDemo_info_name(String demo_info_name)
	{
		this.demo_info_name = demo_info_name;
	}

	
}
